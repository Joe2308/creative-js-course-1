// Javascript operators

// Basic operators

let inbox = 10;
// Add
console.log(inbox + 10); 
// Subtract
console.log(inbox - 10);
// Multiply
console.log(inbox * 10);
// Divide
console.log(inbox / 10);
// Modulos (remainder)
console.log(inbox % 10);
// Exponentiation (to the power of the 2nd number)
console.log(inbox ** 10);

// Increment by 1
inbox++;
console.log(inbox);

// Decrement by 1
inbox--;
console.log(inbox);

// Increment by a specific amount
inbox += 20;
console.log(inbox);

// Decrement by a specific amount
inbox -= 10;
console.log(inbox);